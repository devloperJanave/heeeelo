                        <li><p><b>Role: </b> To recognize and formalize forest rights under the Forest Rights Act, 2006 (FRA) for Scheduled Tribes and other traditional forest dwellers.</p></li>
                        <li><p><b>Formation: </b> Exists in pre-identified villages.</p></li>
                    </ul>
