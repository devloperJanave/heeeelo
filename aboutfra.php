        <p><b>The Forest Rights Act (FRA)</b>, officially known as the Scheduled Tribes and Other Traditional Forest Dwellers (Recognition of Forest Rights) Act, 2006, is a landmark legislation in India. It recognizes and secures the rights of forest-dwelling communities over the land and resources they have traditionally used.</p>
        <p><b>Key Objectives</b></p>
        <ol>
            <li><p><b>Recognizing Land Rights: </b> Provides legal ownership of forest land to Scheduled Tribes (ST) and Other Traditional Forest Dwellers (OTFD) who have been living in forests for generations.</p></li>
            <li><p><b>Ensuring Livelihood Security: </b> Grants communities the right to collect and use forest resources such as firewood, grazing land, and minor forest produce.</p></li>
            <li><p><b>Empowering Local Governance: </b>Strengthens the role of Gram Sabha (village assemblies) in managing and conserving forest resources.</p></li>
            <li><p><b>Supporting Development & Rehabilitation: </b>Recognizes rights in cases where communities are displaced for projects or other purposes.</p></li>
            <li><p><b>Preserving Culture & Ecology: </b>Balances the traditional rights of forest dwellers with conservation goals.</p></li>
            <li><p><b>Ownership Rights: </b>FRA grants ownership rights of up to 4 hectares of land to tribal communities.</p></li>
            <li><p><b>Resource Rights: </b>FRA provides local residents with rights to collect minor forest produce, grazing, and other resources.</p></li>
            <li><p><b>Development and Rehabilitation Rights: </b>FRA also grants rights for development and rehabilitation in designated areas.</p></li>
            <li><p><b>Forest Management Rights: </b>FRA gives the right to protect and conserve forest resources.</p></li>
        </ol>
        <p><b>Importance</b></p>
        <ul>
            <li>FRA helps correct historical injustices faced by forest-dwelling communities by legally recognizing their rights.</li>
            <li>It provides a framework for sustainable forest management while empowering local communities to protect and manage their resources.</li>
        </ul>  

